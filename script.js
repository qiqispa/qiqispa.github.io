
// 첫 번째 동영상

     $('#play-pause-button').click(function () {

     		$("#pre1").hide();

		   var mediaVideo = $("#bgdvid").get(0);
		   var playv = $("#playv");
		   if (mediaVideo.paused) {
		       mediaVideo.play();
		       playv.parent();
		   } else {
		       mediaVideo.pause();
		       playv.parent();
		  }
		});

